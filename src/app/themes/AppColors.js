function hexToRgbWithOpacity(hex, opacity) {
    let result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
    const rgba = result ? `rgba(${parseInt(result[2],16)},${parseInt(result[3],16)},${parseInt(result[1],16)},${opacity})`:null;
    return rgba
}